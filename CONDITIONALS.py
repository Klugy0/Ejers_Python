#par inpar

numero = int(input("Ingresa un número: "))
if numero % 2 == 0:
    print("El número es par.")
else:
    print("El número es impar.")

#edad

edad = int(input("Ingresa tu edad: "))
if edad >= 18:
    print("Eres mayor de edad.")
else:
    print("Eres menor de edad.")

##calificacion

calificacion = int(input("Ingresa tu calificación (0 a 10): "))
if calificacion >= 5:
    print("Has aprobado.")
else:
    print("Has reprobado.")
